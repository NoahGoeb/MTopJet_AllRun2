void jetarea()
{
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- declare files --------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TFile *TTbar_top = new TFile("uhh2.AnalysisModuleRunner.MC.TTbar.root");
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- get mass plots -------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TH1F * area_xcone = (TH1F*)TTbar_top->Get("XCone_subjets/area_all_subjets");
  TH1F * area_ak4 = (TH1F*)TTbar_top->Get("XCone_subjets/area_ak4");


  TH1F *h1 = area_xcone;
  TH1F *h2 = area_ak4;

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------


  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- set up lines and titles ----------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------

  h1->SetTitle("jet area");
  h1->GetXaxis()->SetTitle("jet area");
  h1->GetYaxis()->SetTitle("events");
  h1->GetYaxis()->SetTitleOffset(1.2);
  h2->SetTitle("jet area");
  h2->GetXaxis()->SetTitle("jet area");
  h2->GetYaxis()->SetTitle("events");
  h2->GetYaxis()->SetTitleOffset(1.2);
  h1->SetLineColor(2);
  h2->SetLineColor(4);
  h1->SetLineWidth(4);
  h2->SetLineWidth(4);


  gStyle->SetOptStat(kFALSE);
  gStyle->SetPadTickY(1);
  gStyle->SetPadTickX(1);
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
 

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- draw Hists -----------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TCanvas *A = new TCanvas();
  leg = new TLegend(0.65,0.85,0.85,0.65);
  leg->AddEntry(h1,"xcone subjets","l");
  leg->AddEntry(h2,"AK4 jets","l");
  h2->Draw("HIST");
  h1->Draw("HIST SAME");

  leg->Draw("");
  A->SaveAs("/afs/desy.de/user/s/schwarzd/Plots/XCone_jetarea.pdf");
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------


}
