void lumi_plot()
{
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- declare files --------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TFile *data = new TFile("uhh2.AnalysisModuleRunner.DATA.DATA.root");
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- get mass plots -------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TH1F * lumi[6];
  lumi[0] = (TH1F*)data->Get("02_Trigger_lumi/luminosity");
  lumi[1] = (TH1F*)data->Get("03_Lepton_lumi/luminosity");
  lumi[2] = (TH1F*)data->Get("04_Jet_lumi/luminosity");
  lumi[3] = (TH1F*)data->Get("05_TwoD_lumi/luminosity");
  lumi[4] = (TH1F*)data->Get("06_MET_lumi/luminosity");
  lumi[5] = (TH1F*)data->Get("08_bTag_lumi/luminosity");

  //// ---------------------------------------------------------------------------------------------------------------------



  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- set up lines and titles ----------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------

  gStyle->SetOptStat(kFALSE);
  gStyle->SetPadTickY(1);
  gStyle->SetPadTickX(1);

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
 

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- draw Hists -----------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TCanvas *A = new TCanvas("A", "A", 600, 600);
  gPad->SetLeftMargin(0.15);
  lumi[5]->Draw("E1");
  gPad->RedrawAxis();


  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------


}
