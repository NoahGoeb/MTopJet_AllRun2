void softdrop()
{
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- declare files --------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TFile *TTbar_top = new TFile("uhh2.AnalysisModuleRunner.MC.TTbar.root");
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- get mass plots -------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TH1F * Mass = (TH1F*)TTbar_top->Get("XCone_GEN_GenOnly/Mass_HadJet33");
  TH1F * SD = (TH1F*)TTbar_top->Get("XCone_GEN_GenOnly/SoftDropMass");
  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  cout<<"normal: "<< Mass->Integral() << endl;
  cout<<"SD: "<< SD->Integral() << endl;


  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- set up lines and titles ----------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  int xlow, xhigh;
  int ylow, yhigh;
  xlow = 0;
  xhigh = 450;
  ylow = 0;
  yhigh = 260;

  Mass->SetTitle("XCone Softdrop");
  Mass->GetXaxis()->SetTitle("Leading Jet Mass [GeV]");
  Mass->GetYaxis()->SetTitle("events");
  Mass->GetYaxis()->SetTitleOffset(1.7);
  Mass->GetYaxis()->SetRangeUser(ylow, (Mass->GetMaximum())*1.1);
  Mass->GetXaxis()->SetRangeUser(xlow, xhigh);
  Mass->GetXaxis()->SetNdivisions(505);
  Mass->GetYaxis()->SetNdivisions(505);

  Mass->SetLineColor(1);
  Mass->SetLineWidth(3);

  SD->SetLineColor(kAzure+7);
  SD->SetLineWidth(3);
  // SD->SetLineStyle(9);

  gStyle->SetOptStat(kFALSE);
  gStyle->SetPadTickY(1);
  gStyle->SetPadTickX(1);

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
 

  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------- draw Hists -----------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------
  TCanvas *A = new TCanvas("A", "A", 600, 600);
  gPad->SetLeftMargin(0.15);
  leg = new TLegend(0.5,0.65,0.8,0.8);
  leg->AddEntry(Mass,"combined mass","l");
  leg->AddEntry(SD,"softdrop mass","l");
  Mass->Draw("HIST");
  SD->Draw("HIST SAME");
  leg->Draw("");
  gPad->RedrawAxis();
  A->SaveAs("/afs/desy.de/user/s/schwarzd/Plots/XCone_GEN_SoftDrop.pdf");


  //// ---------------------------------------------------------------------------------------------------------------------
  //// ---------------------------------------------------------------------------------------------------------------------


}
